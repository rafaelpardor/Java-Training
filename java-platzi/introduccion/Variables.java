public class Variables{
  public static void main(String[] args){
    float gravity;
    gravity = 9.8f;
    System.out.println(gravity);

    int salary = 2000;
    String first_name = "Rafael";
    System.out.println(first_name + " " + salary);

    int bounty = 20;
    salary = 3000 + bounty;
    System.out.println(first_name + " " + salary);
  }
}
